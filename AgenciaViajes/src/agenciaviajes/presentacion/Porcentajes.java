    @Override
    public void actualizar(AModel amodel) {
        try {
            GestorClientes gestor = (GestorClientes) amodel;
            ArrayList<Cliente> listado = gestor.consultarClientes();
            int porcentajeHombres, porcentajeMujeres;
            int count = 0;
            for (int i = 0; i < listado.size();i++){
                if (listado.get(i).getSexo().equalsIgnoreCase("Masculino")){
                    count++;
                }
            }
            porcentajeHombres = (count * 100)/listado.size();
            porcentajeMujeres = 100 - porcentajeHombres;
            jLabel1.setText(Integer.toString(porcentajeHombres));
            jLabel2.setText(Integer.toString(porcentajeMujeres));
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(GUIClientesVer.class.getName()).log(Level.SEVERE, null, ex);
            Utilidades.mensajeError("Error al consultar clientes", "AtenciÃ³n");
        }
    }
